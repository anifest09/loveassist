// Loads heavy bold Inter weights for the RIZZ aesthetic.
// (Playfair fonts intentionally NOT loaded — display fonts map to Inter ExtraBold.)
import { useFonts } from "expo-font";
import {
  Inter_400Regular,
  Inter_500Medium,
  Inter_600SemiBold,
  Inter_700Bold,
  Inter_800ExtraBold,
  Inter_900Black,
} from "@expo-google-fonts/inter";

export const useAppFonts = (): readonly [boolean, Error | null] =>
  useFonts({
    Inter_400Regular,
    Inter_500Medium,
    Inter_600SemiBold,
    Inter_700Bold,
    Inter_800ExtraBold,
    Inter_900Black,
  });
